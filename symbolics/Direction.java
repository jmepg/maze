package symbolics;

/**
 * Symbolic constants referring to the hero's direction
 */
public class Direction {

	/** Up direction */
	public static final char UP = 'w';
	public static final int UP_INT = 0;
	/** Down direction */
	public static final char DOWN = 's';
	public static final int DOWN_INT = 1;
	/** Left direction */
	public static final char LEFT = 'a';
	public static final int LEFT_INT = 2;
	/** Right direction */
	public static final char RIGHT = 'd';
	public static final int RIGHT_INT = 3;

}
