package symbolics;

public class Size {

	/**
	 * The standard button height in the program.
	 */
	public static final int buttonHeight = 25;
	/**
	 * The game's vertical size.
	 */
	public static final int vSize = 600;
	/**
	 * The game's horizontal size.
	 */
	public static final int hSize = 600;
	/**
	 * The first vertical coordinate of the maze in creation mode.
	 */
	public static final int createGameYi = buttonHeight;
	/**
	 * The first horizontal coordinate of the maze in creation mode.
	 */
	public static final int createGameXi = 0;
	/**
	 * The panel's horizontal size.
	 */
	public static final int gameHSize = hSize;
	/**
	 * The panel's vertical size.
	 */
	public static final int gameVSize = vSize - 2 * buttonHeight;

}
